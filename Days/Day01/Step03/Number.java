public class Number {
    public int getModulus(int i1, int i2) {
	int result = i1 % i2;
	if (result == 0)
	    return(-1);
	return(result);
    }
    
    public int getMin(int[] i) {
	int result = i[0];
	
	for (int a : i) {
	    if (a < result)
		result = a;
	}
	return(result);
    }

    public int getMax(int[] i) {
	int result = i[0];

	for (int a : i) {
	    if (a > result)
		result = a;
	}
	return(result);
    }

    public int[] sort(int[] tab) {
	return(tab);
    }

    public int getSum(int[] i) {
	int result = i[0];

	for (int a = 1; a < i.length; a++) {
	    result = result + i[a];
	}
	return(result);
    }
}