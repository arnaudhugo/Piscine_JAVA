public class Account {
    private String id;
    private int balance;

    public Account() {
    }

    public String getId() {
	return(this.id);
    }

    public void setId(String id) {
	this.id = id;
    }

    public int getBalance() {
	return(this.balance);
    }

    public void setBalance(int balance) {
	this.balance = balance;
    }
}